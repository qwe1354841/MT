--宠物模板染色
function PetColor.Dyeing_Plan(player,guid,index)
    local pet = player:GetPetContainer(PET_CONTAINER_PANEL):GetPetByGUID(tonumber(guid))
    if not pet then
        sLuaApp:NotifyTipsMsg(player,"宠物未拥有，无法染色！")
        return
    end
    local model = PetColor.GetPetModel(pet)
    local PlanConfig = PetColor.Plan[model][index]
    local all_item_key = {}
    local all_item_list = {}
    local part_num = Lua_tools.tablecount(PetColor.PartConfig[model])

    for i = 1, part_num do
        local plan_config = PlanConfig["Part"..i]
        for k, v in ipairs(plan_config) do
            if type(v) == "string" then
                all_item_key[v] = plan_config[k+1] + (all_item_key[v] or 0)
            end
        end
    end

    for k, v in pairs(all_item_key) do
        table.insert(all_item_list,k)
        table.insert(all_item_list,v)
    end

    local a = Lua_tools.IsItemEnough(player, all_item_list)
    if not a then
        --快速购买
		if OneKeyBuy then
			local defect_consume_tb = Lua_tools.GetDefectList(player, all_item_list)
			if OneKeyBuy.Main(player, defect_consume_tb) then
				return
			end
		end
        sLuaApp:NotifyTipsMsg(player,"染色物品数量不足！")
        return
    end

    for part, _ in ipairs(PetColor.PartConfig[model]) do
        local item_list = PetColor.Plan[model][index]["Part"..part]
        if item_list then
            for i, v in ipairs(item_list) do
                local item = ""
                local num = 1
                if type(v) == "string" then
                    item = v
                    if item ~= "" then
                        if not PetColor.Item[item] then
                            local itemData = ItemConfig.GetByKeyName(item)
                            sLuaApp:NotifyTipsMsg(player,itemData.Name.."无法给宠物染色！")
                            return
                        end
                        if item_list[i+1] then
                            if type(item_list[i+1]) == "number" then
                                num = item_list[i+1]
                            end
                        end
                        local log = sItemSystem:ConsumeItemWithId(player, sItemSystem:GetItemIdByKeyname(item), num, 3, "system", "宠物模板染色", "宠物模板染色消耗")
                        if log ~= 0 then
                            sLuaApp:LuaErr("PetColor.Dyeing_Plan ConsumeItemWithId Err , Log："..log)
                            return false
                        end 
                    end
                end
            end
        end
        local color_list = PetColor.GetPlanColor(player, model,index,part)
        pet:SetString("ColorJson_"..part,Lua_tools.serialize(color_list))
    end

    local json_list = PetColor.GetJsonList(pet,model)
    --sLuaApp:LuaErr(Lua_tools.serialize(json_list))
    pet:SetString("Model_DynJson1",  json.encode(json_list))
    sLogServerSystem:LogToServer(0,player,"宠物模板染色",0,0,pet:GetKeyName(),Lua_tools.serialize(json_list))
    sLuaApp:NotifyTipsMsg(player,"宠物染色成功！")
    local str = [[
        PetDyeingUI_UserDefined.ColorList = ]]..Lua_tools.serialize(json_list)..[[
        PetDyeingUI_UserDefined.Refresh()
    ]]
    sLuaApp:ShowForm(player, "脚本表单", str)
end

function PetColor.GetPlanColor(player,model,plan_index,part)
    plan_index = tonumber(plan_index)
    local item_list = PetColor.Plan[model][plan_index]["Part"..part]
    local color_list = Lua_tools.CopyTable(PetColor.BasalColor[model]["Part"..part])
    if item_list then
        for i, v in ipairs(item_list) do
            local item = ""
            local num = 1
            if type(v) == "string" then
                item = v
                if item ~= "" then
                    if not PetColor.Item[item] then
                        local itemData = ItemConfig.GetByKeyName(item)
                        sLuaApp:NotifyTipsMsg(player,itemData.Name.."无法给宠物染色！")
                        return
                    end
                    if item_list[i+1] then
                        if type(item_list[i+1]) == "number" then
                            num = item_list[i+1]
                        end
                    end
                    for k, list in pairs(PetColor.Item[item]) do
                        if not color_list[k] then
                            color_list[k] = {}
                        end
                        for key, value in pairs(list) do
                            color_list[k][key] = value*num + (color_list[k][key] or 0)
                        end
                    end
                end
            end
        end
    end
    for k, v in pairs(color_list) do
        for n, m in pairs(v) do
            if m < 0 then
                color_list[k][n] = 0
            elseif m > 255 then
                color_list[k][n] = 255
            end 
        end
    end
    return color_list
end

