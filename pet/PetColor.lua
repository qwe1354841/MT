---1-1 变为 0-1
function PetColor.HSVConvert_1(H,S,V)
    if H < 0 then
        H = 0
    end
    --H = (H+1)/2
    S = (S+1)/1.5
    V = (V+1)/1.5
    return H,S,V
end

--0-1 变为 -1-1
function PetColor.HSVConvert_2(H,S,V)
    H = (H*2)-1
    S = (S*2)-1
    V = (V*2)-1
    return H,S,V
end

