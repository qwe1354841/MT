function PetColor.GetJsonList(pet,model)
    local new_list = {}
    local petData = PetConfig.GetByKeyName(pet:GetKeyName())

    for i, value in ipairs(PetColor.PartConfig[model]) do
        if pet:GetString("ColorJson_"..i) ~= "" then
            local list = assert(load("return " .. pet:GetString("ColorJson_"..i)))()
            if PetColor.ColorType[model] == 1 and PetColor.IsBasalColor(pet) then
                local a = {}
                local IsHSV = false
                if petData.ColorId == 0 then
                    a = Lua_tools.DupTable(PetColor.BasalColor[model]["Part"..i])
                else
                    local ColorData = ColorConfig.GetById(petData.ColorId)
                    if ColorValueList[ColorData.AppearId] and ColorValueList[ColorData.AppearId]["IsFillPart"..i] == "True" then
                        a["h"] = tonumber(ColorValueList[ColorData.AppearId]["HOffset"..i])
                        a["s"] = tonumber(ColorValueList[ColorData.AppearId]["SOffset"..i])
                        a["v"] = tonumber(ColorValueList[ColorData.AppearId]["VOffset"..i])
                        IsHSV = true
                    else
                        a = Lua_tools.DupTable(PetColor.BasalColor[model]["Part"..i])
                    end
                end
                local JsonList = PetColor.GetColorJsonList_1(list,a,i,IsHSV)
                for k, v in pairs(JsonList) do
                    new_list[k] = v
                end
            else
                local JsonList = PetColor.GetColorJsonList_2(list,i)
                for k, v in pairs(JsonList) do
                    new_list[k] = v
                end
            end
        else
            if petData.ColorId ~= 0 then
                local color_list = {}
                local ColorData = ColorConfig.GetById(petData.ColorId)
                if ColorValueList[ColorData.AppearId] and ColorValueList[ColorData.AppearId]["IsFillPart"..i] == "True" then
                    local H = tonumber(ColorValueList[ColorData.AppearId]["HOffset"..i])
                    local S = tonumber(ColorValueList[ColorData.AppearId]["SOffset"..i])
                    local V = tonumber(ColorValueList[ColorData.AppearId]["VOffset"..i])
                    new_list["IsFillPart"..i] = true
                    new_list["HOffset"..i] = H
                    new_list["SOffset"..i] = S
                    new_list["VOffset"..i] = V

                    H,S,V = PetColor.HSVConvert_1(H,S,V)
                    color_list["Color"] = {}
                    color_list["Color"]["r"],color_list["Color"]["g"],color_list["Color"]["b"] =  Lua_tools.HSV2RGB(H,S,V)
                    pet:SetString("ColorJson_"..i,Lua_tools.serialize(color_list))
                end
            end
        --    new_list["IsAddColorFirst"..i] = false
        --    new_list["IsFillPart"..i] = false
        end
    end
    return new_list
end

