--属性加成升级
function Guard_Add.Attr_Levelup(player,guard_id)
    if player == nil or guard_id == nil then
        return
    end
    guard_id = tonumber(guard_id)
    
    local GuardContainer = player:GetGuardContainer()
    local guard = GuardContainer:GetGuardById(guard_id)

    if guard == nil then
        sLuaApp:NotifyTipsMsg(player , '您未拥有该侍从')
        return
    end

    local Attr_Add_Level = guard:GetInt("Attr_Add_Level")
    local GuardStar = GuardUpStar.GetGuardStar(player,guard:GetGUID())
    if Attr_Add_Level <= 5 then
        if Attr_Add_Level >= GuardStar then
            sLuaApp:NotifyTipsMsg(player , '加成已达当前最高，请先升级侍从')
            return
        end
    else
        sLuaApp:NotifyTipsMsg(player , '加成已达最高级')
        return
    end
    local Levelup = false
    if Attr_Add_Level == 0 then
        for i = 1, 2 do
            local Attr_id = Guard_Add.Attr_Add_Config[guard_id]["S1Attr"..i]
            if Attr_id ~= 0 then
                local Next_Attr_Num = Guard_Add.Attr_Add_Config[guard_id]['S1Attr'..i..'Num']
                if player:SetDynAttr(guard_id,Attr_id,Next_Attr_Num) then
                    guard:SetInt("Attr_Add_Level",Attr_Add_Level+1)
                    guard:SetInt("Attr_Add_AttrId",Attr_id)
                    guard:SetInt("Attr_Add_AttrNum",Next_Attr_Num)
                    --记录日志
                    sLogServerSystem:LogToServer(0, player, "侍从加成等级", Attr_Add_Level , 1 , "侍从key:"..guard:GetKeyName(), "属性加成激活")
                    Levelup = true
                    player:RecalcAttr()
                else
                    sLuaApp:NotifyTipsMsg(player , '属性加成失败')
                end
            end
        end
        if Levelup then
            local Levelup_NextAtrr = 0
            local New_Attr_Add_Level = guard:GetInt("Attr_Add_Level")
            local Config = Guard_Add.Attr_Add_Config[guard_id]
            Levelup_NextAtrr = Config['S'..(New_Attr_Add_Level+1)..'Attr1Num']

            local FightValueList = {}
            FightValueList["PlayerValue"] = player:GetAttr(ROLE_ATTR_FIGHT_VALUE)
            FightValueList["PetValue"] = player:RecalcTop5PetFightValue()
            FightValueList["GuardValue"] = player:RecalcTop4GuardFightValue()
            local str = [[
                if GuardUI then
                    GuardUI.CombatPower = ]]..Lua_tools.serialize(FightValueList)..[[
                    GuardUI.AttAttrPage_Next_Attr_Num = ]]..Levelup_NextAtrr..[[
                    GuardUI.RefreshAddAttrAllPage()
                end
            ]]

            -- 更新侍从小红点数据
            if FormGuard then
                FormGuard.get_guard_red_point_data(player)
            end
            --sLuaApp:LuaDbg(str)
            sLuaApp:ShowForm(player, "脚本表单", str)
            sLuaApp:NotifyTipsMsg(player , '激活成功')
        end
    elseif Attr_Add_Level <= 5 and Attr_Add_Level ~= 0 then
        for i = 1, 2 do
            local Attr_id = Guard_Add.Attr_Add_Config[guard_id]["S"..Attr_Add_Level..'Attr'..i]
            if Attr_id ~= 0 then
                local Now_Attr_Num = Guard_Add.Attr_Add_Config[guard_id]["S"..Attr_Add_Level..'Attr'..i..'Num']
                local Next_Attr_Num = Guard_Add.Attr_Add_Config[guard_id]["S"..(Attr_Add_Level+1)..'Attr'..i..'Num']
                if player:SetDynAttr(guard_id,Attr_id,Next_Attr_Num) then
                    guard:SetInt("Attr_Add_Level",Attr_Add_Level+1)
                    guard:SetInt("Attr_Add_AttrId",Attr_id)
                    guard:SetInt("Attr_Add_AttrNum",Next_Attr_Num)
                    --记录日志
                    sLogServerSystem:LogToServer(0, player, "侍从加成等级", Attr_Add_Level , 1 , "侍从key:"..guard:GetKeyName(), "属性加成升级")
                    Levelup = true
                    player:RecalcAttr()
                else
                    sLuaApp:NotifyTipsMsg(player , '属性加成失败')
                end
            end
        end
        if Levelup then
            local Levelup_NextAtrr = 0
            local New_Attr_Add_Level = guard:GetInt("Attr_Add_Level")
            local Config = Guard_Add.Attr_Add_Config[guard_id]
            if New_Attr_Add_Level <= 5 then
                Levelup_NextAtrr = Config['S'..(New_Attr_Add_Level+1)..'Attr1Num']
            else
                Levelup_NextAtrr = Config['S'..(New_Attr_Add_Level)..'Attr1Num']
            end
            local FightValueList = {}
            FightValueList["PlayerValue"] = player:GetAttr(ROLE_ATTR_FIGHT_VALUE)
            FightValueList["PetValue"] = player:RecalcTop5PetFightValue()
            FightValueList["GuardValue"] = player:RecalcTop4GuardFightValue()
            local str = [[
                if GuardUI then
                    GuardUI.CombatPower = ]]..Lua_tools.serialize(FightValueList)..[[
                    GuardUI.AttAttrPage_Next_Attr_Num = ]]..Levelup_NextAtrr..[[
                    GuardUI.RefreshAddAttrAllPage()
                end
            ]]

            -- 更新侍从小红点数据
            if FormGuard then
                FormGuard.get_guard_red_point_data(player)
            end
            --sLuaApp:LuaDbg(str)
            sLuaApp:ShowForm(player, "脚本表单", str)
            sLuaApp:NotifyTipsMsg(player , '升级成功')
        end
    end
end

--得到侍从加成数据
function Guard_Add.GetGuardAddData(Guard_Guid)
    if Guard_Guid == nil then
        return
    end
    local Guard = sGuardSystem:GetGuardByGUID(tonumber(Guard_Guid))
    local Add_Level = Guard:GetInt("Attr_Add_Level")
    local Guard_Id = Guard:GetId()
    local GuardAddData = {}
    if Add_Level == 0 then
        GuardAddData["Level"] = 1
        GuardAddData["Attr"] = {}
        GuardAddData["Attr"]["Id"] = Guard_Add.Attr_Add_Config[Guard_Id]["S1Attr1"]
        GuardAddData["Attr"]["Num"] = Guard_Add.Attr_Add_Config[Guard_Id]["S1Attr1Num"]
    else
        GuardAddData["Level"] = Add_Level
        GuardAddData["Attr"] = {}
        GuardAddData["Attr"]["Id"] = Guard_Add.Attr_Add_Config[Guard_Id]["S"..Add_Level.."Attr1"]
        GuardAddData["Attr"]["Num"] = Guard_Add.Attr_Add_Config[Guard_Id]["S"..Add_Level.."Attr1Num"]
    end
    return GuardAddData
end

