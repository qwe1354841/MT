--侍从加成属性数据
function Guard_Add.MainData(player)
    if player == nil then
        return
    end
    local Attr_List = {}
    local GuardContainer = player:GetGuardContainer()
    for k, v in pairs(Guard_Add.Attr_Add_Config) do
        local a = {}
        local guard = GuardContainer:GetGuardById(k)
        if guard ~= nil then
            local guard_Star = GuardUpStar.GetGuardStar(player,guard:GetGUID())
            a['id'] = k
            local Attr_Add_Level = guard:GetInt("Attr_Add_Level")
            a['Attr_Add_Level'] = Attr_Add_Level
            a['Attr_id'] = v['S'..guard_Star..'Attr1']
            if Attr_Add_Level == 0 then
                a['Now_Attr_Num'] = 0
            else
                a['Now_Attr_Num'] = v['S'..Attr_Add_Level..'Attr1Num']
            end

            if Attr_Add_Level <= 5 then
                a['Next_Attr_Num'] = v['S'..(Attr_Add_Level+1)..'Attr1Num']
            else
                a['Next_Attr_Num'] = 0
            end
        else
            a['id'] = k
            a['Attr_Add_Level'] = 0
            a['Attr_id'] = v['S1Attr1']
            a['Now_Attr_Num'] = 0
            a['Next_Attr_Num'] = v['S1Attr1Num']
        end
        table.insert(Attr_List,a)
    end

    local str = [[
        if GuardUI then
            GuardUI.GuardAddAttrData = ]]..Lua_tools.serialize(Attr_List)..[[
            GuardUI.RefreshGuardAddItem()
        end
    ]]

    --sLuaApp:LuaDbg(str)
    sLuaApp:ShowForm(player, "脚本表单", str)
end
